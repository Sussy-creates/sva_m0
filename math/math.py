# Primative Types
subject = "Bob"
print(f"Hello {subject}!") # String
print(15) # Integers (Number without decimals)
print(15.5432) # Float (Numbers with decimals)
print(34 + 23j) # Complex Number (Real + Imaginary)
print(True) # Boolean (True/False only)

# Operators
num = 8 
print(f"{num} + {num} =", num + num) #10
print(f"{num} - {num} =", num - num) #0
print(f"{num} * {num} =", num * num) #25
print(f"{num} / {num} =", num / num) #1

print(f"{num}**2 =", num**2) # Exponent / Power
print(f"{num} // 2 =", num // 2) # Integer Division (rownds down to nearest whole number)
print("4 % 2 =", 4 % 2) # Modulus (returns the remainder of the first argument divided by the second)

