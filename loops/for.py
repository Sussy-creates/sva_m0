# For Loop
# Similalr looping mechanism

# Type 1:
# for item in GROUP_OF_ITEMS:
#       DO SOMETHING

# TYpe 2:
# for item in rang(RANGE OF NUMBERS):
#       DO SOMETHING

# Iterate through numbers

# For loop manages your condition for you
for n in range(100):
        print(n)
else:
        print("Done")
      
# Iterating through a string
for char in "Hello World!":
    print(char)

# While loop keeps running until condition changes
# You have to manually change the condition
n = 100
while n < 100:
        print(n)
        n += 1 # Shorthand for n = n + 1
